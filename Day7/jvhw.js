let rainbow = ['red', 'yellow', 'green', 'blue', 'navy', 'purple', 'black']

//index가 2인 값 출력
console.log(rainbow[2]);
// 좋아하는 색을 마지막에 추가한 후 배열 출력
rainbow.push('mint');
console.log(rainbow);
// black이 몇 번째 인덱스에 있는지 출력
console.log( rainbow.indexOf('black') );
// 리스트의 순서를 반전시켜서 출력 ( 반전은 안 배운 함수! 찾아보기 )
console.log(rainbow.reverse());
